def analyze_books(books_data: list[dict[str, Any]]) -> None:
    """Analyze book data for duplicates, genres, and publication years."""
    from collections import defaultdict, Counter

    # Initial summary
    print(f"\n{' Book Data Analysis ':=^50}\n")
    print(f"Total books: {len(books_data)}")

    # Find duplicate books by name
    name_counts = defaultdict(list)
    for book in books_data:
        name_counts[book["name"]].append(book)

    duplicates = [entries for entries in name_counts.values() if len(entries) > 1]

    if duplicates:
        print(f"\n{' Duplicates Found ':-^50}")
        print(f"Total duplicate titles: {len(duplicates)}")
        print("Duplicate entries:")
        for i, group in enumerate(duplicates, 1):
            print(f"\nDuplicate group {i} ({len(group)} occurrences):")
            for dup in group:
                print(f"  - {dup['name']} (ID: {dup.get('id', 'N/A')}, Year: {dup['year']})")
    else:
        print("\nNo duplicate titles found.")

    # Genre analysis
    all_genres = [genre for book in books_data for genre in book["genres"]]
    genre_counts = Counter(all_genres)
    sorted_genres = sorted(genre_counts.items(), key=lambda x: (-x[1], x[0]))

    print(f"\n{' Genre Analysis ':-^50}")
    print(f"Unique genres: {len(genre_counts)}")
    print("\nGenre frequency (sorted by count then name):")
    for genre, count in sorted_genres:
        print(f"  {genre:<20}: {count:>3}")

