    all_years = []
    for book in books_data:
        year = book["year"]
        if isinstance(year, str) and year.isdigit():
            all_years.append(int(year))
        elif isinstance(year, (int, float)):
            all_years.append(int(year))

    if not all_years:
        print("\nWarning: No valid publication years found in the data")
        return

    year_counts = Counter(all_years)
    sorted_years = sorted(year_counts.items(), key=lambda x: x[0])  # Sort by year

