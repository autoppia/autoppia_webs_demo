    print(f"Most common genre: {sorted_genres[0][0]} ({sorted_genres[0][1]} books)")

    # Get the year with most publications (sorted by count descending, then year ascending)
    most_common_years = sorted(year_counts.items(), key=lambda x: (-x[1], x[0]))
    print(f"Year with most publications: {most_common_years[0][0]} ({most_common_years[0][1]} books)")

